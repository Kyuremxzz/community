/* Glifos normalizados: 5 linhas, largura fixa por glifo. */
const FONT: Record<string, readonly string[]> = (() => {
  const out: Record<string, string[]> = {};
  for (const k of Object.keys(RAW_FONT)) {
    const rows = RAW_FONT[k]!;
    let w = 0;
    for (let i = 0; i < rows.length; i++) w = Math.max(w, rows[i]!.length);
    const norm: string[] = [];
    for (let i = 0; i < 5; i++) {
      let ln = rows[i] ?? '';
      while (ln.length < w) ln += ' ';
      norm.push(ln);
    }
    out[k] = norm;
  }
  return out;
})();

const SPACE_GLYPH: readonly string[] = FONT[' ']!;

/** Retorna array de 5 linhas com o texto em fonte grande. */
export function bigLines(text: string): string[] {
  const rows = ['', '', '', '', ''];
  for (let i = 0; i < text.length; i++) {
    const ch = text.charAt(i);
    const g = FONT[ch] ?? FONT[ch.toUpperCase()] ?? SPACE_GLYPH;
    for (let r = 0; r < 5; r++) {
      rows[r] += (i > 0 ? ' ' : '') + g[r]!;
    }
  }
  return rows;
}

/** Banner figlet — retorna string multilinha. */
export function title(text: string): string {
  return bigLines(text).join('\n');
}
